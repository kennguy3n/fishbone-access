func TestSync_PaginatesUsers(t *testing.T) {
	calls := 0
	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		calls++
		if !strings.HasPrefix(r.Header.Get("Authorization"), "Bearer ") {
			t.Errorf("expected Bearer auth, got %q", r.Header.Get("Authorization"))
		}
		if calls == 1 {
			users := make([]map[string]interface{}, 0, 2)
			for i := 0; i < 2; i++ {
				users = append(users, map[string]interface{}{
					"id":    fmt.Sprintf("u%d", i+1),
					"name":  fmt.Sprintf("User %d", i+1),
					"email": fmt.Sprintf("u%d@x.com", i+1),
				})
			}
			b, _ := json.Marshal(map[string]interface{}{
				"users": users,
				"meta":  map[string]interface{}{"links": map[string]interface{}{"next": "cursor2"}},
			})
			_, _ = w.Write(b)
			return
		}
		if r.URL.Query().Get("cursor") != "cursor2" {
			t.Errorf("cursor = %q", r.URL.Query().Get("cursor"))
		}
		_, _ = w.Write([]byte(`{"users":[{"id":"u3","name":"Last","email":"last@x.com"}],"meta":{"links":{"next":""}}}`))
	}))
	t.Cleanup(srv.Close)
	c := New()
	c.urlOverride = srv.URL
	c.httpClient = func() httpDoer { return srv.Client() }
	var got []*access.Identity
	err := c.SyncIdentities(context.Background(), validConfig(), validSecrets(), "", func(b []*access.Identity, _ string) error {
		got = append(got, b...)
		return nil
	})
	if err != nil {
		t.Fatalf("Sync: %v", err)
	}
	if len(got) != 3 {
		t.Fatalf("len = %d", len(got))
	}
	if calls < 2 {
		t.Fatalf("expected pagination, calls = %d", calls)
	}
}

func TestConnect_Failure(t *testing.T) {
	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		w.WriteHeader(http.StatusUnauthorized)
	}))
	t.Cleanup(srv.Close)
	c := New()
	c.urlOverride = srv.URL
	c.httpClient = func() httpDoer { return srv.Client() }
	if err := c.Connect(context.Background(), validConfig(), validSecrets()); err == nil || !strings.Contains(err.Error(), "401") {
		t.Errorf("Connect err = %v; want 401", err)
	}
}

