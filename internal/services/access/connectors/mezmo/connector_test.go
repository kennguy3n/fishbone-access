func TestSync_PaginatesUsers(t *testing.T) {
	// Mezmo /v1/config/members returns a single page; verify the connector
	// emits the full set in one batch and the handler runs once.
	calls := 0
	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		calls++
		if !strings.HasPrefix(r.Header.Get("Authorization"), "servicekey ") {
			t.Errorf("expected servicekey auth, got %q", r.Header.Get("Authorization"))
		}
		if r.URL.Path != "/v1/config/members" {
			t.Errorf("path = %q", r.URL.Path)
		}
		members := []map[string]interface{}{
			{"_id": "m1", "email": "a@x.com", "name": "Alice", "role": "owner"},
			{"_id": "m2", "email": "b@x.com", "name": "Bob", "role": "member", "disabled": true},
		}
		b, _ := json.Marshal(members)
		_, _ = w.Write(b)
	}))
	t.Cleanup(srv.Close)
	c := New()
	c.urlOverride = srv.URL
	c.httpClient = func() httpDoer { return srv.Client() }
	var got []*access.Identity
	batches := 0
	err := c.SyncIdentities(context.Background(), validConfig(), validSecrets(), "", func(b []*access.Identity, _ string) error {
		batches++
		got = append(got, b...)
		return nil
	})
	if err != nil {
		t.Fatalf("Sync: %v", err)
	}
	if len(got) != 2 || batches != 1 || calls != 1 {
		t.Fatalf("got=%d batches=%d calls=%d", len(got), batches, calls)
	}
	if got[1].Status != "disabled" {
		t.Errorf("status = %q", got[1].Status)
	}
}

func TestConnect_Failure(t *testing.T) {
	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, _ *http.Request) {
		w.WriteHeader(http.StatusUnauthorized)
	}))
	t.Cleanup(srv.Close)
	c := New()
	c.urlOverride = srv.URL
	c.httpClient = func() httpDoer { return srv.Client() }
	if err := c.Connect(context.Background(), validConfig(), validSecrets()); err == nil || !strings.Contains(err.Error(), "401") {
		t.Errorf("Connect err = %v; want 401", err)
	}
}

func TestGetCredentialsMetadata_RedactsToken(t *testing.T) {
	md, err := New().GetCredentialsMetadata(context.Background(), validConfig(), validSecrets())
	if err != nil {
		t.Fatalf("err: %v", err)
	}
	short, _ := md["key_short"].(string)
	if short == "" || strings.Contains(short, "AAAA1234") {
		t.Errorf("key_short = %q", short)
	}
}
