func TestSync_PaginatesUsers(t *testing.T) {
	calls := 0
	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		calls++
		if !strings.HasPrefix(r.Header.Get("Authorization"), "ApiToken ") {
			t.Errorf("expected ApiToken auth, got %q", r.Header.Get("Authorization"))
		}
		if calls == 1 {
			_, _ = w.Write([]byte(`{"data":[{"id":"u1","email":"a@b.com","fullName":"Alice","role":"admin","isActive":true}],"pagination":{"nextCursor":"c2","totalItems":2}}`))
			return
		}
		if r.URL.Query().Get("cursor") != "c2" {
			t.Errorf("cursor = %q", r.URL.Query().Get("cursor"))
		}
		_, _ = w.Write([]byte(`{"data":[{"id":"u2","email":"b@b.com","fullName":"Bob","role":"viewer","isActive":false}],"pagination":{"nextCursor":null,"totalItems":2}}`))
	}))
	t.Cleanup(srv.Close)
	c := New()
	c.urlOverride = srv.URL
	c.httpClient = func() httpDoer { return srv.Client() }
	var got []*access.Identity
	err := c.SyncIdentities(context.Background(), validConfig(), validSecrets(), "", func(b []*access.Identity, _ string) error {
		got = append(got, b...)
		return nil
	})
	if err != nil {
		t.Fatalf("Sync: %v", err)
	}
	if len(got) != 2 {
		t.Fatalf("len = %d", len(got))
	}
	if calls < 2 {
		t.Fatalf("expected pagination, calls = %d", calls)
	}
	if got[1].Status != "inactive" {
		t.Errorf("expected inactive, got %q", got[1].Status)
	}
}

